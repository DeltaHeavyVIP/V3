package data;

public class Data {

    private double a;
    private double b;
    private double e;
    private int met;
    private int equation;
    private String out;

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getE() {
        return e;
    }

    public int getMet() {
        return met;
    }

    public int getEquation() {
        return equation;
    }

    public String getOut() {
        return out;
    }

    public void setA(double a) {
        this.a = a;
    }

    public void setB(double b) {
        this.b = b;
    }

    public void setE(double e) {
        this.e = e;
    }

    public void setMet(int met) {
        this.met = met;
    }

    public void setEquation(int equation) {
        this.equation = equation;
    }

    public void setOut(String out) {
        this.out = out;
    }
}
